	<h2>Macke</h2>
	
    <p>Sumirajući sve možemo zaključiti da su mačke potpuno drugačije od pasa. One vas neće upozoriti na nepoznate ljude u bližoj okolini, većina mačaka ne voli šetati sa vlasnicima, žive svojim tempom i potpuno su samostalne. Mačke ne teže tome da uče trikove i zadovoljavaju svoje vlasnike, odnosno prilično su narcisoidne, samodovoljne i fokusirane na lične želje.</p>

	<p>Nuždu obavljaju unutar stana, tako da je svakodnevno čišćenje i te kako potrebno, a sam miris nije nimalo ugodan. Jednom sedmično se mora mijenjati pijesak gdje obavljaju nuždu. Tu se ogleda prednost pasa koje možete naučiti da sve obavljaju vani za vrijeme šetnje.</p>
	<img src="macke.jpg" height="260" width="290">
    <p>Nezavisnost mačke kao kućnog ljubimca zna biti i prednost, jer nećete morati brinuti kada napustite stan zbog posla i obaveza. Ne zavise od vas, ali vas zbog toga i ne iščekuju željno da dođete kući.</p>

	<p>Iako i mačke vole igru, ne traže toliko energije kao psi, niti će sami pohrliti prema vama da se družite. Više su noćni tip, što znači da su tada najaktivnije. Celi dan uglavnom ljenčare i spavaju, a zabavu traže tokom noći, kada padne mrak. Vrlo je moguće da, ako imaju prostora za napuštanje doma, nazad vrate neki plen, što i nije najbolje ukoliko imate decu.</p>

	<h1>Pas ili macka</h1>
	<h2>Prednosti i mane pasa</h2>
	<div align="center"><img src="psi.jpg" alt="lineweb" width="593" height="331">">
	  </div>
	<p>Kao što smo već prethodno naveli, psi oduzimaju mnogo više vremena nego mačke. Njih treba redovno šetati, i to ne samo ukoliko živite u stanu, već i ako imate kuću sa dvorištem. Šetnje su im korisne i sa psihološkog i za fizičkog aspekta, a uz to je moguće da ih naučite da u tom periodu obavljaju nuždu kako bi vaš dom uvijek bio čist.</p>

	<p>Ukoliko živite u stanu, potrebne su barem tri šetnje u toku dana. Vremenski uslovi ne treba da vas sputavaju, pogotovo jer vašem psu neće smetati ni snijeg ni kiša. I ovo zavisi od pasmine, jer su veći psi uglavnom spremni na akciju, dok se manji više vole ušuškati u toplini doma. Uzimajući navedeno u obzir, šetnje treba da budu nešto šta volite, jer ako nisu – teško da ćete se prilagoditi njihovim potrebama i željama, a samim tim pas nije najbolji kućni ljubimac za vas.</p>

	<p>Takođe, psi treba da provode kvalitetno vrijeme sa vama. Vlasnici pasa su njima sve, a ljubav koju pokazuju i radosno čekanje vašeg dolaska kući je iz dana u dan sve veće. Zato je poželjno i dobro da se igrate sa njima, da ih mazite i da im pokažete jednaku ljubav kao onu koju oni vama pružaju. Psi su vezani za vlasnika, uopšte im nije bitno gdje su dok god su sa vama, dok su mačke vezane za sam prostor, odnosno dom.</p>
	<p><img src="psi1.jpg" alt="simple.pic" width="693" height="431">Psi vole biti uključeni u sve šta radite. Ma koliko ti možda zvučalo nepogodno, zapravo ima i veliki broj prednosti. Može da vas prati uvijek i svuda, čak i da vam pomogne, ali najviše od svega da bude odlično društvo. S obzirom na veliku želju da udovolje svojim vlasnicima, psi su usljed toga i poslušniji. Nastrojeni su zaštitnički i uvijek upozoravaju kada se neko nepoznat kreće po vašoj sredini.</p>

	<p>Finansijska zahtjevnost je na strani pasa. Oni jedu više hrane, žele više igračaka, često ih treba četkati i kupati. Naravno, bitno je postaviti granice i okvire, uvesti ustaljene mjere i pravila, jer oni su ti koji vas slušaju i prilagođavaju se vašem životu u domu.</p>

	<p>Pas je veoma inteligentna životinja sa kojom nećete imati mnogo problema. Možete ga naučiti zgodnim trikovima, a one najinteligentnije i do 200 riječi i naredbi. Uvijek možete potražiti i profesionalne trenere da ih obuče, pa se i najživahniji i najtvrdoglaviji mogu obuzdati.
Sve u svemu, psi su najbolje i najzahtjevnije životinjsko društvo koje možete imati.</p>
	
